# range(): Crea una lista inmutable de numeros en sucesion aritmetica

numeros = range(5)  #[0, 1, 2, 3, 4]
print(numeros[1])

numeros1 = range(4,10)  #[4, 5, 6, 7, 8, 9]
print(numeros1[5])

numeros2 = range(10,100,8)   #[10, 18, 26, 34, 443, 50, 58, 66, 74, 82,, 90, 98]
print(numeros2[9])
